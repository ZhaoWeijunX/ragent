# 工单查询场景补充规则（ticket_query）

## region（地区）
- 「华东」「华南」「华北」「西南」「西北」→ 原样输出
- 「全国」「各地区」→ 不输出 region
- 未提及 → 不输出

## status（工单状态）
- 「待处理」「未处理」「还没处理」→ `待处理`
- 「处理中」「正在处理」→ `处理中`
- 「已解决」「解决了」→ `已解决`
- 「已关闭」「关闭了」「关闭的」→ `已关闭`
- 未提及 → 不输出

## priority（优先级）
- 「紧急」「加急」「火急」→ `紧急`
- 「高优先级」「高的」→ `高`
- 「中」「中等」→ `中`
- 「低」「低的」→ `低`
- 未提及 → 不输出

## product（产品）
- 「企业版」「专业版」「基础版」→ 原样输出
- 未提及 → 不输出

## customerName（客户名称）
- **强制规则**：用户问题中出现公司/客户/企业名称时，**必须**输出 `customerName`，不得遗漏。
- 示例：「腾讯科技有哪些紧急工单」→ `"customerName": "腾讯科技", "priority": "紧急", "queryType": "list"`
- 示例：「腾讯科技的工单进展如何」→ `"customerName": "腾讯科技"`
- 「阿里巴巴」「华为」等客户名原样提取，去掉「的」等助词
- 未提及任何客户/公司名 → 不输出

## queryType（查询类型）
- 「有哪些」「列出」「哪些工单」「工单明细」→ **必须** `list`（尤其当同时指定了客户名时）
- 「统计」「分析」「分类」「解决率」「占比」「排名」→ `stats`
- 「概览」「汇总」「多少」「总数」「概况」「进展」→ `summary`
- 未明确类型 → `summary`（可不输出 queryType）

## limit（返回条数）
- 「前10条」「列5个」→ 提取数字
- 未提及 → 不输出（使用默认 10）

