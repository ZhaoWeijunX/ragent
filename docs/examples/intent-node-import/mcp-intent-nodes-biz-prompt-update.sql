# 销售查询场景补充规则（sales_query）

## region（地区）
- 「华东」「华南」「华北」「西南」「西北」→ 原样输出对应中文
- 「全国」「各地区」「所有地区」→ 不输出 region（查全国）
- 未提及地区 → 不输出

## period（时间段）
- 「本月」「这个月」→ `本月`
- 「上月」「上个月」→ `上月`
- 「本季度」「这个季度」「Q1/Q2/Q3/Q4（当前季度）」→ `本季度`
- 「上季度」「上个季度」→ `上季度`
- 「今年」「本年」「年度」→ `本年`
- 未提及时间 → 不输出（使用默认 `本月`）

## product（产品）
- 「企业版」「专业版」「基础版」→ 原样输出
- 未提及 → 不输出

## salesPerson（销售人员）
- 提取人名，如「张三的业绩」「李四卖了多少」→ `"salesPerson": "张三"`
- 未提及 → 不输出

## queryType（查询类型）
- 「排名」「排行」「TOP」「前几名」「谁卖得最好」→ `ranking`
- 「明细」「列表」「订单详情」「哪些客户」→ `detail`
- 「趋势」「走势」「按周」「变化」→ `trend`
- 「总额」「汇总」「统计」「多少」「总计」「平均」→ `summary`
- 仅问销售额/销量且未明确类型 → `summary`（可不输出 queryType）

## limit（返回条数）
- 「前5名」「TOP 10」「列 20 条」→ 提取数字
- 中文数字转阿拉伯数字：「前五」→ `5`
- 未提及 → 不输出（使用默认 10）

# 输出要求

**格式**：严格合法的 JSON 对象，无尾逗号，无 JSON 外文字

**示例**：
{"region": "华东", "period": "本月", "queryType": "summary"}
{"region": "华南", "period": "上月", "queryType": "ranking", "limit": 5}
{"salesPerson": "张三", "period": "本年", "queryType": "detail", "limit": 10}$PROMPT$,
